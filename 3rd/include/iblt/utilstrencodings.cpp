signed char HexDigit(char c)
{
    return p_util_hexdigit[(unsigned char)c];
}

vector<uint8_t> ParseHex(const char* psz)
{
    // convert hex dump to vector
    vector<uint8_t> vch;
    while (true)
    {
        while (isspace(*psz))
            psz++;
        signed char c = HexDigit(*psz++);
        if (c == (signed char)-1)
            break;
        uint8_t n = (c << 4);
        c = HexDigit(*psz++);
        if (c == (signed char)-1)
            break;
        n |= c;
        vch.push_back(n);
    }
    return vch;
}

vector<uint8_t> ParseHex(const string& str)
{
    return ParseHex(str.c_str());
}
